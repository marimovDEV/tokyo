export interface MenuItem {
  id: string
  name: string
  nameUz: string
  nameRu: string
  description: string
  descriptionUz: string
  descriptionRu: string
  price: number
  image: string
  category: string
  available: boolean
  prepTime?: string // e.g., "15-20"
  rating?: number // e.g., 4.8
  ingredients: string[]
  ingredientsUz: string[]
  ingredientsRu: string[]
}

export interface Category {
  id: string
  name: string
  nameUz: string
  nameRu: string
  icon: string
  image: string
}

export interface CartItem extends MenuItem {
  quantity: number
  notes?: string
}

export interface Order {
  id: string
  tableNumber: number
  items: CartItem[]
  total: number
  status: "pending" | "preparing" | "ready" | "served"
  timestamp: Date
  customerName?: string
}

export interface Promotion {
  id: string
  title: string
  titleUz: string
  titleRu: string
  description: string
  descriptionUz: string
  descriptionRu: string
  image: string
  active: boolean
  link?: string
  category?: string
}

export interface Review {
  id: string
  name: string
  surname: string
  comment: string
  rating: number
  date: string
  approved: boolean
}

